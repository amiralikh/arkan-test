Route::group(['prefix' => 'panel','middleware' => 'auth'],function (){
   Route::get('/',[\App\Http\Controllers\Panel\HomeController::class,'index'])->name('panel.index');
   Route::resource('users',\App\Http\Controllers\Panel\UserController::class);
   Route::resource('posts',\App\Http\Controllers\Panel\PostController::class);
});

Route::get('/', function () {
    return Inertia::render('Welcome', [
        'canLogin' => Route::has('login'),
        'canRegister' => Route::has('register'),
        'laravelVersion' => Application::VERSION,
        'phpVersion' => PHP_VERSION,
    ]);
});

Route::get('/dashboard', function () {
    return Inertia::render('Dashboard');
})->middleware(['auth', 'verified'])->name('dashboard');

require __DIR__.'/auth.php';
